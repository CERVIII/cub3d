#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	if (!s)
		return ;
	i = 0;
	while (i < ft_strlen(s) && s[i])
	{
		(*f)(i, &s[i]);
		i++;
	}
}
